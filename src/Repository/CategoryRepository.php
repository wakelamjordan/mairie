            ->leftJoin('c.roles', 'r')
            ->andWhere('r.label IN (:roles) OR r.label IS NULL')  // Ajout de la condition pour les rÃ´les null
            ->setParameter('roles', $roles)
            ->orderBy('c.rank', 'ASC')
            ->getQuery()
            ->getResult();
    
